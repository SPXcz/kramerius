package cz.incad.kramerius.rest.apiNew.client.v70.redirection;

public class ProxyHandlerException extends Exception {

	public ProxyHandlerException() {
		super();
	}

	public ProxyHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProxyHandlerException(String message) {
		super(message);
	}

	public ProxyHandlerException(Throwable cause) {
		super(cause);
	}

	
}
